CREATE TABLE
    IF NOT EXISTS cfb.game_team_stats (
        game_id INT,
        team_id INT,
        team VARCHAR(100),
        completion_attempts VARCHAR(10) NULL,
        first_downs INT NULL,
        fourth_down_eff VARCHAR(10) NULL,
        fumbles_lost INT NULL,
        fumbles_recovered INT NULL,
        interception_tds INT NULL,
        interception_yards INT NULL,
        interceptions INT NULL,
        kick_return_tds INT NULL,
        kick_return_yards INT NULL,
        kick_returns INT NULL,
        kicking_points INT NULL,
        net_passing_yards INT NULL,
        passes_intercepted INT NULL,
        passing_tds INT NULL,
        possession_time VARCHAR(10) NULL,
        rushing_attempts INT NULL,
        rushing_tds INT NULL,
        rushing_yards INT NULL,
        third_down_eff VARCHAR(10) NULL,
        total_penalties_yards VARCHAR(10) NULL,
        total_yards INT NULL,
        turnovers INT NULL,
        yards_per_pass FLOAT NULL,
        yards_per_rush_attempt FLOAT NULL,
        punt_return_tds INT NULL,
        punt_return_yards INT NULL,
        punt_returns INT NULL
    );