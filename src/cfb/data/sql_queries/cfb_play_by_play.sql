CREATE TABLE IF NOT EXISTS cfb.play_by_play (
    id                 BIGINT,
    drive_id           BIGINT,
    game_id            INT,
    drive_number       INT,
    play_number        INT,
    offense            VARCHAR(50),
    offense_conference VARCHAR(50),
    offense_score      INT,
    defense            VARCHAR(50),
    home               VARCHAR(50),
    away               VARCHAR(50),
    defense_conference VARCHAR(50),
    defense_score      INT,
    period             INT,
    offense_timeouts   INT NULL,
    defense_timeouts   INT NULL,
    yardline           INT,
    yards_to_goal      INT,
    down               INT,
    distance           INT,
    yards_gained       INT,
    scoring            BOOLEAN,
    play_type          VARCHAR(100),
    play_text          TEXT,
    ppa                FLOAT,
    wallclock          TIMESTAMP NULL,
    clock_minutes      INT,
    clock_seconds      INT,
    PRIMARY KEY (id),
    FOREIGN KEY (game_id) REFERENCES cfb.games(id) ON DELETE SET NULL
);
